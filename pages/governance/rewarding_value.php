                <h1 class="text-uppercase">Rewarding value creation</h1>
                <p>Clicks Group’s remuneration policy is aimed at driving a high-performance culture that creates sustainable value for shareholders.</p>
                <p>The remuneration policy, which is outlined in part 1 of this report, will again be proposed to shareholders for a non-binding vote at the annual general meeting (AGM) in January 2019. The application of the remuneration policy in 2018, which details how the group has rewarded value creation, is covered in part 2 of this report. In accordance with the King IV governance code, this implementation report will be tabled separately to shareholders for a non-binding vote at the AGM.</p>
                <p>Clicks Group values the views and insights of investors, and encourages shareholders to proactively engage with management on remuneration issues to enable informed decisions to be made when voting on the group’s remuneration policy.</p>
                <p class="margin-bottom-5px">In addition to this commitment, and in accordance with King IV, in the event that either the remuneration policy or the implementation report receives 25% or more dissenting votes, management will engage directly with these shareholders to:</p>
                <ul class="margin-top-0">
                    <li>determine the reasons for the dissenting votes; and</li>
                    <li>address legitimate and reasonable objections or concerns by clarifying or amending the remuneration policy, its implementation or processes, or reviewing the remuneration governance, or taking other steps to resolve the concerns.</li>
                </ul>
                <p>The steps taken to address legitimate and reasonable concerns will be disclosed in the following years’ integrated report.</p>
                <p>The remuneration philosophy and reward principles remain consistent with last year when the remuneration policy was aligned to King IV to outline the group’s approach to fair, responsible and transparent remuneration practices across the business. At the 2018 AGM, 93.9% of shareholders who voted supported the group’s remuneration policy and 94.6% supported the group’s remuneration implementation report.</p>
                <p class="margin-bottom-40px">This report provides an overview of the remuneration of all group employees as well as disclosing executive director remuneration and the alignment with shareholder value creation. The remuneration paid to executive and non-executive directors for the 2018 financial year is detailed on <a href="">pages 75 and 77</a>.</p>

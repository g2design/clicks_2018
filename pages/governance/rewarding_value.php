                <h2 class="heading-3 white sky-blue-bg text-uppercase box-padding-smaller margin-bottom-20px text-center">Part 1: Remuneration policy</h2>
                <h5 class="margin-bottom-0">Introduction</h5>
                <p class="margin-bottom-5px">The group’s remuneration policy is based on the total rewards model and integrates the five key elements that attract, motivate and retain human capital to achieve the desired business results:</p>
                <ul class="margin-top-0">
                    <li>compensation;</li>
                    <li>benefits;</li>
                    <li>performance and recognition;</li>
                    <li>learning and development; and</li>
                    <li>work-life integration.</li>
                </ul>
                <p>The reward principles of fair and responsible remuneration, market competitiveness, and pay-for-performance are entrenched in the policy. The policy is transparent and incorporates a pay framework that clearly differentiates between occupational levels and pay grades that facilitate remuneration benchmarking for each job within a skill pool.</p>
                <p>The remuneration mix includes a combination of monetary and non-monetary rewards for employees in exchange for their time, efforts, talent and performance at an individual, team and company level.</p>
                <p>Monetary rewards include annual guaranteed pay, variable pay such as short and long-term incentives that relate to performance to agreed targets, as well as other benefits.</p>
                <p>Non-monetary rewards are less tangible and range from formal and informal recognition programmes, training and job rotation opportunities and exposure to stimulating work assignments, all of which are designed to motivate, affiliate and retain employees.</p>
                <p>Employees receive a total reward statement annually which provides a personalised comprehensive view of all their rewards.</p>
                <p>Pay levels are benchmarked on national and retail market benchmark data. The 2018 benchmarking process and the resultant pay framework was peer reviewed by independent reward consultants (21st Century) who verified the accuracy of the benchmarking process and outcomes, as well as compliance to King IV. Premiums are paid for scarce and critical skills such as pharmacy, buying and planning, finance and IT skills, based on the relevant market data.</p>
                <p>Annual salary increases are merit based, with increases being directly related to each employee’s annual performance rating. The range of increase percentages per performance rating is applied consistently across the group, including to the executive directors. The annual increase for an employee in the bargaining unit is based on a collective bargaining process (refer to the <a href="">section on remuneration of management&nbsp;and&nbsp;staff</a>).</p>
                <h5 class="margin-bottom-0">Remuneration structure</h5>
                <p>The total rewards framework provides flexibility to meet the differing needs of employees.</p>
                <p class="margin-bottom-5px">Annual guaranteed pay is determined by considering the following factors:</p>
                <ul class="margin-top-0">
                    <li>the size of the job, based on the Hay job evaluation methodology;</li>
                    <li>the nature of the job relative to its defined market position, including any market premiums for scarce and critical skills;</li>
                    <li>individual performance as assessed during the performance review process; and</li>
                    <li>individual position in the pay band range relative to competence and talent positioning.</li>
                </ul>
                <p>The remuneration and nominations committee (the committee) reviews the group’s overall pay framework annually against defined market benchmarks per job grade, job size or skill pool.</p>
                <p>The group’s benchmarking and market information is based on independent surveys, including the PricewaterhouseCoopers REMchannel, Deloitte Top Executive and The Korn Ferry surveys. These benchmarking exercises recognise the complexity in the group’s business model and the regulatory environment within which the group operates.</p>
                <p>The group also participates in a biennial benchmarking exercise to maintain a competitive remuneration position in respect of pharmacists and pharmacy managers.</p>
                <p class="margin-bottom-5px">The annual performance review of all employees focuses on both financial and non-financial levers across the following metrics:</p>
                <ul class="margin-top-0">
                    <li>Financial performance</li>
                    <li>Business process improvement metrics, including transformation targets, where this can be influenced by the employee</li>
                    <li>Customer satisfaction</li>
                    <li>Learning and growth</li>
                </ul>
                <p>Executives are also measured against the objectives set by the social and ethics committee in relation to all the elements of the BBBEE scorecard.</p>
                <p>All employees are required to achieve a satisfactory performance rating to qualify for full participation in the shortterm incentive scheme.</p>
                <h4 class="sky-blue">Executive directors' remuneration</h4>

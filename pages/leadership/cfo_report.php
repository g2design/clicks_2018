                <p>The performance for the 2018 financial year is within the target ranges for most metrics, excluding the return on equity and the return on assets which have been impacted in the short term by the group’s broad-based employee share ownership scheme. These ratios are expected to trend upwards after the scheme matures in the 2019 financial year.</p>
                <p>Management has introduced a net working capital target to replace the inventory target in order to provide guidance on broader working capital efficiency across the group. The operating margin target range for the distribution segment has been increased from 2.2% – 2.7% to 2.5% – 3.0% to reflect the expected margin benefit from UPD securing additional bulk distribution contracts.</p>
                <p>The group’s medium-term financial targets rank in the upper quartile relative to comparable global health and beauty retailers such as Walgreens Boots Alliance (USA), CVS (USA), Raia Drogasil (Brazil) and Celesio (Germany).</p>
                <h4 class="sky-blue">Financial performance</h4>
                <p>The following review of the group’s financial performance for the year ended 31 August 2018 focuses on the key line items of the statements of comprehensive income and financial position which management consider material to the group’s performance.</p>
                <p>The following review should be considered together with the <a href="">annual financial statements</a> as well as the <a href="">summary statements of comprehensive income and financial position</a>, and the <a href="">five-year analysis of financial performance</a>.</p>
                <h4 class="sky-blue">Statement of comprehensive income</h4>
                <h5 class="margin-bottom-0"><b>Turnover</b></h5>
                <p>Group turnover increased by 9.1% to R29.2 billion (2017: R26.8 billion). Selling price inflation for the group averaged only 1.9% for the year compared to 5.3% in the previous year.</p>
                <p>Turnover was again relatively consistent across the first and second halves of the year. There is generally minimal seasonal effect on the group’s turnover as the festive season in the first half of the financial year is counter-balanced by the winter season, which is the peak trading period for the health and wellness business. However, the incidence of colds and flu in 2018 was lower than last winter which depressed medicine sales.</p>
                <p>Retail turnover, including Clicks, The Body Shop, GNC, Claire’s and Musica, increased by 10.8%. Retail selling price inflation was low at only 1.4%. Comparable stores sales grew by 5.5%.</p>
                <p>Within the retail division, health and beauty sales increased by 11.7%, reflecting the resilient nature of the core Clicks business. Musica’s sales were 8.4% lower owing to the pressure on discretionary consumer spending and the increase in digital consumption of music and movies.</p>
                <p>Growth in retail trading space accounted for 5.3% of the turnover growth with the net opening of 42 retail stores and 37 pharmacies.</p>
                <p>Distribution turnover grew by 8.4% as UPD experienced a moderately stronger second half performance.</p>
                <p>The trading performances of Clicks and UPD are covered in the <a href="">business review</a>.</p>
                <h5 class="margin-bottom-0"><b>Total income</b></h5>
                <p>Total income grew by 10.5% to R7.9 billion (2017: R7.1 billion).</p>
                <p>The retail total income margin expanded by 20 basis points to 33.6% by optimising the promotional product mix and effectively managing promotional campaigns.</p>
                <p>UPD’s total income margin was impacted by the low increase in the single exit price (SEP) of medicine of only 1.26% in March 2018 compared to 7.5% in March 2017. However, the impact was partially off-set by UPD onboarding three new distribution clients in the second half of the year and the margin declined by 10 basis points to 7.3%.</p>
                <p>Owing to the favourable mix impact from the faster growth in retail, the group’s total income margin strengthened by 30 basis points to 27.0%.</p>
                <h5 class="margin-bottom-0"><b>Operating expenditure</b></h5>
                <p>Retail operating expenditure as a percentage of turnover improved to 25.5% from 25.6% in the prior year.</p>
                <p>Retail expense growth of 10.3% was contained below sales growth despite the extensive investment in new stores and pharmacies. Comparable retail costs increased by 5.1%, reflecting the tight management of costs.</p>
                <p>UPD continued to demonstrate excellent cost control with expenses up 6.5% on last year notwithstanding the increased labour and transport costs relating to the onboarding of the three new distribution contracts.</p>
                <p>The group’s operating expenses increased by 9.7%.</p>
                <h5 class="margin-bottom-0"><b>Operating profit</b></h5>
                <p>Operating profit increased by 12.6% to R2.0 billion (2017: R1.8 billion) as both the retail and distribution businesses achieved operating leverage and benefited from increased scale. The group margin strengthened by 20 basis points to 7.0%.</p>
                <p class="margin-bottom-40px">Retail increased its operating profit margin by 30 basis points to 8.1% and grew operating profit by 14.7% despite a reduction of R19 million in Musica’s profit. UPD maintained its operating margin at 2.7% despite the low SEP increase.</p>
